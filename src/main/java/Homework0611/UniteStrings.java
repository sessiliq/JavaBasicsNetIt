package Homework0611;
/*
Unite two or more strings in one and write it in the console.
 */
public class UniteStrings {
    public static void main(String[] args) {

        String firstString = "I am student at ";
        String secondString = "Software ";
        String thirdString = "Academy!";

        String wholeString = firstString + secondString + thirdString;

        System.out.println(wholeString);
    }
}
